<p>At present this means that 73.2% of the population of the districts
	within Hampshire County Council live in Fairtrade districts.
	Considering the ceremonial county, including the unitary authorities of
	Portsmouth and Southampton, the current proportion of the population
	within Fairtrade zones is 80.9%.
