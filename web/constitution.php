<h1>Constitution for the Hampshire Fairtrade Network</h1>
<p><b>Hampshire Fairtrade Network</b> is the operating name of the
Network. The area covered by the Network is the ceremonial county of
Hampshire - i.e. those areas which elect members of Hampshire County
Council, Portsmouth City Council and Southampton City Council.</p>
<h2>Aims</h2>
<ol><li>To promote Fairtrade in Hampshire.
<li>To alleviate poverty by
campaigning for a fair system of international trade.
<li>To gain and
maintain recognition of Hampshire as a Fairtrade County.
</ol>
<h2>Membership</h2>
<p>Membership of the Network is open to all groups within its area which
are recognised by the Fairtrade Foundation. There is no membership fee.
A group is entitled to cast one vote on matters on which a ballot is
called.
<h2>Steering Group</h2>
<p>The Network shall be organised by a steering
group consisting of a Chair, Secretary and Treasurer. Members of the
steering group are to be elected at an Annual General Meeting. All
members shall be given at least eight weeks' notice of the AGM.
<h2>Equal Opportunities</h2>
<p>All the Network's activities, including regular meetings,
will be open to all, irrespective of age, gender, ethnic origin, sexual
orientation or disability. The Network will ensure, as far as
practicable, that all its activities are held in venues that are
accessible to all.
<h2>Finance</h2>
<p>All monies will be paid into a bank account
in the name of the Network and the account will be operated on behalf of
the Network. The funds will be used to further the aims of the Network.
The annual accounts shall be presented to the Annual General Meeting. On
dissolution of the Network, any remaining assets shall be donated to the
Fairtrade Foundation.
<h2>Changes to the Constitution</h2>
<p>The constitution may
only be changed by agreement of two thirds of those voting at a meeting
called for that purpose for which at least eight weeks' notice is given.
